// JAD-201:主题注册表代码生成器(运行时按主题裁剪的单一事实来源)。
//
// 同一份配置同时产出两种注册表:
//   - 全主题(签入文件 theme-registry.jsx,renderDeck 默认 / 多主题 deck 用)。
//   - 裁剪版(renderDeck 按一份 deck 实际用到的主题集合,临时生成后用 esbuild 别名注入)。
//
// 这样一份单主题 deck 的 imported-theme-runtime.js 只内嵌它那一个主题的页面组件,
// 而非全部 12 个主题(体积 ~1/12,且只暴露一个主题)。

// 已接入主题(与 src/components/themes/theme* 目录、generated-metadata 的 themeKey 一致)。
export const ALL_THEME_KEYS = [
  'theme01', 'theme02', 'theme03', 'theme04', 'theme05', 'theme06',
  'theme07', 'theme08', 'theme09', 'theme10', 'theme11', 'theme12',
];

// 各主题(可选)的图片槽 React context。没有 context 的主题(02/04/05/07/12)不出现在此表里,
// 它们在 wrapThemeImageProviders 中不包裹任何 Provider。valueKey 对应 client-runtime 传入的
// values 对象字段:theme01Value / theme03Value / keyedValue / theme11Value。
export const THEME_IMAGE_CONTEXTS = {
  theme01: { module: 'theme01/source/slides/SlideKit.jsx', exportName: 'ImageSlotActions', localAlias: 'theme01ImageSlotActions', valueKey: 'theme01Value' },
  theme03: { module: 'theme03/source/src/ImageSlot.jsx', exportName: 'ImageSlotMediaContext', localAlias: 'theme03ImageSlotMediaContext', valueKey: 'theme03Value' },
  theme06: { module: 'theme06/source/slides/kit.jsx', exportName: 'KxImageSlotMediaContext', localAlias: 'theme06KxImageSlotMediaContext', valueKey: 'keyedValue' },
  theme08: { module: 'theme08/source/components/AclPrimitives.jsx', exportName: 'AdaptiveImageSlotMediaContext', localAlias: 'theme08AdaptiveImageSlotMediaContext', valueKey: 'keyedValue' },
  theme09: { module: 'theme09/source/slides/ImageStrip.jsx', exportName: 'ImageStripMediaContext', localAlias: 'theme09ImageStripMediaContext', valueKey: 'keyedValue' },
  theme10: { module: 'theme10/source/components/DeckImageSlot.jsx', exportName: 'DeckImageSlotMediaContext', localAlias: 'theme10DeckImageSlotMediaContext', valueKey: 'keyedValue' },
  theme11: { module: 'theme11/source/ignBase.jsx', exportName: 'IgnisImageSlotMediaContext', localAlias: 'theme11ImageSlotMediaContext', valueKey: 'theme11Value' },
};

// 把任意主题集合归一为 ALL_THEME_KEYS 顺序、去重、仅保留已接入主题。
export function normalizeThemeKeys(themeKeys) {
  const wanted = new Set(themeKeys || []);
  return ALL_THEME_KEYS.filter(key => wanted.has(key));
}

export function isFullThemeSet(themeKeys) {
  const normalized = normalizeThemeKeys(themeKeys);
  return normalized.length === ALL_THEME_KEYS.length;
}

// JAD-203:每主题预构建模块(`<themeKey>.module.mjs`)的虚拟入口源码。把该主题的
// `runtimePages` 与(可选)图片槽 context 一起 re-export,供 build-theme-runtime 预构建成
// 单文件 minified 模块(react external);安装版不再需要可读的主题 *.jsx 源即可链接。
export function buildThemeModuleEntrySource(themeKey, { importPrefix = './' } = {}) {
  const [key] = normalizeThemeKeys([themeKey]);
  if (!key) throw new Error(`buildThemeModuleEntrySource: not an accepted theme key: ${themeKey}`);
  const ctx = THEME_IMAGE_CONTEXTS[key];
  const lines = [
    `export { runtimePages } from '${importPrefix}${key}/runtime.jsx';`,
  ];
  if (ctx) lines.push(`export { ${ctx.exportName} } from '${importPrefix}${ctx.module}';`);
  return lines.join('\n') + '\n';
}

// 生成注册表模块源码。
//   themeKeys:要包含的主题(会被归一)。
//   importPrefix:import 路径前缀。签入文件用相对前缀 './';临时裁剪文件用指向仓库的绝对前缀。
//   fromModules:为 true 时从预构建的 `<importPrefix><themeKey><moduleSuffix>` minified 模块引入
//     (安装版无可读主题源时的链接路径),而非各主题的 runtime.jsx + 源 context 文件。
export function buildThemeRegistrySource(themeKeys, { importPrefix = './', generated = true, fromModules = false, moduleSuffix = '.module.mjs' } = {}) {
  const keys = normalizeThemeKeys(themeKeys);
  if (!keys.length) throw new Error('buildThemeRegistrySource: no accepted theme keys to include.');
  const contextThemes = keys.filter(key => THEME_IMAGE_CONTEXTS[key]).map(key => THEME_IMAGE_CONTEXTS[key]);

  const header = generated
    ? '// @generated by scripts/build/update-theme-registry.mjs — 请勿手改;改配置见 theme-registry-codegen.mjs。\n'
    : '';
  const lead = [
    '// JAD-201:主题注册表 —— client-runtime 经 `@dashi/theme-registry` 引入,',
    '// renderDeck 打包时把别名指向本文件(全主题)或裁剪版(单/少主题 deck)。',
  ].join('\n');

  const contextImports = contextThemes
    .map(ctx => `import { ${ctx.exportName} as ${ctx.localAlias} } from '${importPrefix}${ctx.module}';`)
    .join('\n');
  const pagesImports = keys
    .map(key => `import { runtimePages as ${key}Pages } from '${importPrefix}${key}/runtime.jsx';`)
    .join('\n');
  // fromModules:每主题一行,从预构建模块同时取 runtimePages 与(可选)context。
  const moduleImports = keys
    .map(key => {
      const ctx = THEME_IMAGE_CONTEXTS[key];
      const names = [`runtimePages as ${key}Pages`];
      if (ctx) names.push(`${ctx.exportName} as ${ctx.localAlias}`);
      return `import { ${names.join(', ')} } from '${importPrefix}${key}${moduleSuffix}';`;
    })
    .join('\n');
  const importsSection = fromModules
    ? moduleImports
    : `${contextImports ? `${contextImports}\n` : ''}${pagesImports}`;
  const spreads = keys.map(key => `  ...${key}Pages,`).join('\n');

  // 包裹各主题的图片槽 Provider:从最内层(配置末位主题)向外逐层包裹,保持
  // 与原 client-runtime 一致的嵌套顺序(theme01 在最外层)。无 context 主题则原样返回。
  const wrapBody = contextThemes.length
    ? [
        '  let tree = element;',
        ...[...contextThemes].reverse().map(ctx =>
          `  tree = React.createElement(${ctx.localAlias}.Provider, { value: values.${ctx.valueKey} }, tree);`),
        '  return tree;',
      ].join('\n')
    : '  return element;';

  return `${header}${lead}
import React from 'react';
${importsSection}

export const runtimePages = [
${spreads}
];

export function wrapThemeImageProviders(element, values) {
${wrapBody}
}
`;
}
