export const GENERATED_HEADER = '// @generated by `npm run metadata:update` — DO NOT EDIT BY HAND. 改生成物无效,请改 theme 源后重新生成。\n\n';

export function formatThemeMetadataModule(theme, pages) {
  return `${GENERATED_HEADER}export const theme = ${JSON.stringify(stripPageCount(theme), null, 2)};\nexport const pages = ${JSON.stringify(pages, null, 2)};\n`;
}

export function formatGeneratedMetadataModule(themes, pages) {
  return `${GENERATED_HEADER}export const GENERATED_THEME_PACKS = ${JSON.stringify(themes, null, 2)};\n\nexport const GENERATED_THEME_PAGES = ${JSON.stringify(pages, null, 2)};\n`;
}

function stripPageCount(theme) {
  const { pageCount, ...rest } = theme;
  return rest;
}
