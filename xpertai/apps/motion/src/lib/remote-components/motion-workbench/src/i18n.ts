export type LocaleKey = keyof typeof DICTIONARY
export type Translator = ReturnType<typeof createTranslator>

export function createTranslator(locale?: string | null) {
  const zhHans = String(locale || '').toLowerCase().startsWith('zh')
  const language = zhHans ? 'zh-Hans' : 'en'
  return (key: LocaleKey, params?: Record<string, string | number>) => interpolate(DICTIONARY[key]?.[language] ?? DICTIONARY[key]?.en ?? key, params)
}

export function localizeOptions<T extends { value: string; labelKey: LocaleKey }>(options: ReadonlyArray<T>, t: Translator) {
  return options.map((option) => ({ value: option.value, label: t(option.labelKey) }))
}

export function labelForTab(tab: string, t: Translator) {
  if (tab === 'projects') return t('projects')
  if (tab === 'library') return t('library')
  if (tab === 'html') return t('html')
  if (tab === 'video') return t('video')
  if (tab === 'versions') return t('versions')
  if (tab === 'exports') return t('exports')
  return tab
}

export function labelForTrigger(trigger: string, t: Translator) {
  if (trigger === 'load') return t('triggerLoad')
  if (trigger === 'scroll') return t('triggerScroll')
  if (trigger === 'hover') return t('triggerHover')
  if (trigger === 'click') return t('triggerClick')
  return trigger
}

export function labelForStatus(status: string | null | undefined, t: Translator) {
  if (!status) return t('draft')
  const normalized = status.toLowerCase()
  if (normalized === 'draft') return t('statusDraft')
  if (normalized === 'reviewed') return t('statusReviewed')
  if (normalized === 'archived') return t('statusArchived')
  if (normalized === 'ready') return t('statusReady')
  return status
}

function interpolate(value: string, params?: Record<string, string | number>) {
  if (!params) {
    return value
  }
  return value.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key) => (params[key] === undefined ? match : String(params[key])))
}
