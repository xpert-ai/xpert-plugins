export type MessageKey = keyof typeof messages.en

export function normalizeLocale(locale: string | undefined): LocaleKey {
  return locale?.toLowerCase().startsWith('zh') ? 'zh' : 'en'
}

export function translate(locale: LocaleKey, key: MessageKey, values?: Record<string, string | number>) {
  let value: string = messages[locale][key] ?? messages.en[key]
  if (!values) {
    return value
  }
  Object.entries(values).forEach(([name, replacement]) => {
    value = value.replaceAll(`{${name}}`, String(replacement))
  })
  return value
}

export function statusText(status: string | undefined, locale: LocaleKey) {
  const key = status === 'reviewed' || status === 'archived' || status === 'draft' ? status : 'noStatus'
  return translate(locale, key)
}

