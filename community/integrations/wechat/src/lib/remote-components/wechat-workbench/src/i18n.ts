const dictionaries = {
  zh_Hans: zhHans,
  en_US: enUS
}

export type TranslationKey = keyof typeof zhHans

export function createTranslator(locale: unknown) {
  const dictionary = dictionaries[normalizeLocale(locale)]
  return (key: TranslationKey) => dictionary[key] || zhHans[key] || key
}

export function normalizeLocale(locale: unknown): keyof typeof dictionaries {
  const value = String(locale || '').toLowerCase()
  if (value.startsWith('en')) {
    return 'en_US'
  }
  return 'zh_Hans'
}
